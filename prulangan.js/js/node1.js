// // Fungsi untuk menghitung nilai akhir
function jumlahkanNA(harian, uts, uas) {
    var nilaiHarian = harian * 0.2;
    var nilaiUTS = uts * 0.3;
    var nilaiUAS = uas * 0.5;
    var na = nilaiHarian + nilaiUTS + nilaiUAS; // Menghitung nilai akhir
    return na; // Mengembalikan nilai akhir
}

// Menghitung nilai akhir
